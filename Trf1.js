const soma = (num1, num2) => {
    const res = num1 + num2;
    return res;
}

const subt = (num1, num2) => {
    const res = num1 - num2;
    return res;
}

const divs = (num1, num2) => {
    const res = num1 / num2;
    return res;
}

const mult = (num1, num2) => {
    const res = num1 * num2;
    return res;
}

console.log(soma(1, 9));

console.log(subt(199, 10));

console.log(divs(145, 79));

console.log(mult(14, 99));